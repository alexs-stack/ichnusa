        function setLang(lang) {
            if (!T[lang]) return;
            document.documentElement.lang = lang === 'de' ? 'de' : (lang === 'en' ? 'en' : 'it');
            document.title = T[lang]['meta.title'];
            document.querySelectorAll('[data-i18n]').forEach(el => {
                const key = el.getAttribute('data-i18n');
                if (T[lang][key] !== undefined) el.innerHTML = T[lang][key];
            });
            document.querySelectorAll('[data-i18n-alt]').forEach(el => {
                const key = el.getAttribute('data-i18n-alt');
                if (T[lang][key] !== undefined) el.alt = T[lang][key];
            });
            document.querySelectorAll('.lang a[data-lang]').forEach(a => {
                a.classList.toggle('active', a.getAttribute('data-lang') === lang);
            });
            try { localStorage.setItem('ic.lang', lang); } catch(e) {}
        }

        const savedLang = (function() { try { return localStorage.getItem('ic.lang'); } catch(e) { return null; } })();
        const initialLang = savedLang && T[savedLang] ? savedLang : (function() {
            const nav = (navigator.language || 'it').slice(0,2).toLowerCase();
            return T[nav] ? nav : 'it';
        })();
        setLang(initialLang);

        document.querySelectorAll('[data-lang]').forEach(a => {
            a.addEventListener('click', e => {
                e.preventDefault();
                setLang(a.getAttribute('data-lang'));
            });
        });

